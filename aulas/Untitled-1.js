02-switch.html
const estadoSul = "SC";
switch (estadoSul) {
  case "RS":
    console.log("Rio Grande do Sul");
    break;
  case "SC":
    console.log("Santa Catarina");
    break;
  case "PR":
    console.log("Paraná");
    break;
  default:
    console.log("Não é sigla de um estado da região sul");
}